"""
Python = Linguagem de Programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples
print('Lucas Manesco')
print('Lucas "Manesco"')

# Aspas duplas
print("Lucas Manesco")
print("Lucas 'Manesco'")

# Escape
print('Lucas \"Manesco\"')

# r
print(r"Lucas \"Manesco\"")